// Grade calculator

let score= 87;

if( score>=90 ){
    console.log("Grade A+ : Excellent");
}
else if( score>=80 ){
    console.log("Grade A : Very Good");
}
else if( score>=70 ){
    console.log("Grade B : Good");
}
else if( score>=60 ){
    console.log("Grade C : Average");
}
else if( score>=50 ){
    console.log("Grade D : Below Average");
}
else if( score>=40 ){
    console.log("Grade E : Poor");
}
else{
    console.log("Grade F : Fail")
}